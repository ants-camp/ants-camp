#!/usr/bin/env bash
# =============================================================================
# Assistant 통합 테스트용 시드 문서 등록 스크립트
#
# 사용법:
#   chmod +x docs/seed-documents.sh
#   ./docs/seed-documents.sh
#
# 옵션 (환경 변수):
#   ASSISTANT_URL   기본값 http://localhost:8096
#   USER_ROLE       기본값 MANAGER (DocumentController 가 MANAGER/MASTER 만 허용)
#   SKIP_DUPLICATE  "true" 면 동일 title 이 이미 있으면 등록 스킵 (기본 false)
#   WAIT_INGEST     "true" 면 모든 문서의 chunkCount > 0 까지 폴링 (기본 true)
#   POLL_TIMEOUT    인제스트 대기 최대 초 (기본 90)
#
# 의존성: bash, curl, jq
# =============================================================================
set -euo pipefail

ASSISTANT_URL="${ASSISTANT_URL:-http://localhost:8096}"
USER_ROLE="${USER_ROLE:-MANAGER}"
SKIP_DUPLICATE="${SKIP_DUPLICATE:-false}"
WAIT_INGEST="${WAIT_INGEST:-true}"
POLL_TIMEOUT="${POLL_TIMEOUT:-90}"

# ---- 컬러 출력 --------------------------------------------------------------
if [[ -t 1 ]]; then
  C_RED=$'\033[0;31m'; C_GRN=$'\033[0;32m'; C_YEL=$'\033[0;33m'
  C_BLU=$'\033[0;34m'; C_DIM=$'\033[2m';     C_RST=$'\033[0m'
else
  C_RED=; C_GRN=; C_YEL=; C_BLU=; C_DIM=; C_RST=
fi
# 로그는 모두 stderr 로 — 함수에서 stdout 으로 doc_id 만 반환할 수 있게.
log()  { printf '%s[seed]%s %s\n' "$C_BLU" "$C_RST" "$*" >&2; }
ok()   { printf '%s[ ok ]%s %s\n' "$C_GRN" "$C_RST" "$*" >&2; }
warn() { printf '%s[warn]%s %s\n' "$C_YEL" "$C_RST" "$*" >&2; }
err()  { printf '%s[err ]%s %s\n' "$C_RED" "$C_RST" "$*" >&2; }

# ---- 의존성 체크 ------------------------------------------------------------
for cmd in curl jq; do
  command -v "$cmd" >/dev/null 2>&1 || { err "필수 명령어가 없습니다: $cmd"; exit 1; }
done

# ---- 서비스 헬스 대기 -------------------------------------------------------
log "assistant-service 헬스 대기: $ASSISTANT_URL"
for i in $(seq 1 60); do
  if curl -fsS "$ASSISTANT_URL/actuator/health" >/dev/null 2>&1; then
    ok "assistant-service 응답 확인"
    break
  fi
  sleep 2
  if [[ "$i" -eq 60 ]]; then
    err "120초 동안 헬스 응답 없음. 컨테이너 상태를 확인하세요."
    exit 1
  fi
done

# ---- 헬퍼: 문서 등록 / 조회 / 폴링 -----------------------------------------
H_ROLE=(-H "X-User-Role: $USER_ROLE")
H_JSON=(-H 'Content-Type: application/json')

list_titles() {
  curl -sS "${H_ROLE[@]}" "$ASSISTANT_URL/api/assistants/documents" \
    | jq -r '.data.items[]?.title'
}

post_document() {
  local title="$1" type="$2" content="$3"

  if [[ "$SKIP_DUPLICATE" == "true" ]] && list_titles | grep -Fxq "$title"; then
    warn "이미 존재 — 스킵: $title"
    return 0
  fi

  local body
  body=$(jq -n --arg t "$title" --arg ty "$type" --arg c "$content" \
        '{title:$t, type:$ty, content:$c}')

  local resp doc_id status
  resp=$(curl -sS -w '\n%{http_code}' -X POST \
        "$ASSISTANT_URL/api/assistants/documents" \
        "${H_ROLE[@]}" "${H_JSON[@]}" -d "$body")
  status=$(printf '%s' "$resp" | tail -n1)
  doc_id=$(printf '%s' "$resp" | sed '$d' | jq -r '.data.documentId // empty')

  if [[ "$status" != "201" || -z "$doc_id" ]]; then
    err "등록 실패 ($status): $title"
    printf '%s\n' "$resp" | sed '$d' >&2
    return 1
  fi

  ok "등록: $title  ${C_DIM}($doc_id)${C_RST}"
  printf '%s\n' "$doc_id"
}

wait_chunked() {
  local doc_id="$1" title="$2"
  local deadline=$((SECONDS + POLL_TIMEOUT))
  while (( SECONDS < deadline )); do
    local cc
    cc=$(curl -sS "${H_ROLE[@]}" \
         "$ASSISTANT_URL/api/assistants/documents/$doc_id" \
         | jq -r '.data.chunkCount // 0')
    if [[ "$cc" =~ ^[0-9]+$ ]] && (( cc > 0 )); then
      ok "인제스트 완료: $title (chunks=$cc)"
      return 0
    fi
    sleep 2
  done
  warn "인제스트 미완료 (timeout=${POLL_TIMEOUT}s): $title"
  return 1
}

# ---- 시드 문서 정의 ---------------------------------------------------------
# 형식: TYPE|TITLE|CONTENT  (CONTENT 내 개행은 \n 으로 표기, 스크립트가 변환)
DOCUMENTS=()

add_doc() {
  DOCUMENTS+=("$1|$2|$(printf '%b' "$3")")
}

